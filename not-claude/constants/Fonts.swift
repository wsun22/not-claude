import SwiftUI

extension Font {
    // mainly for code it seems
    static func firaCode() -> Font {
        return Font.custom("FiraCode-Regular", size: 16)
    }
    
    static func tienne() -> Font {
        return Font.custom("Tienne-Regular", size: 16)
    }
    
    static func styreneB() -> Font {
        return Font.custom("StyreneB", size: 16)
    }
}

